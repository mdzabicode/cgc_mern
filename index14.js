// conditional Statements
// Conditional statements allow you to control the flow of your program based on a condition. If the
// condition is true, the code inside the if statement block will be executed. Otherwise, the code
// inside the else statement (if it exists) will be executed.
// learning about the Switch statement
// if else if ladder
// fall though
var agg = 81;
switch (agg){
    case 75:
        console.log("Distinction");
        break;
    
    case 70:
        console.log("First class");
        break;

    case 60:
        console.log("Second class");
        break;
    
    case 50:
        console.log("Third class");
        break;

    default:
        console.log("Failed");
        break;
}


